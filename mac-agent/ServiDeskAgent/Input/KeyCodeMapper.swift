import Foundation

/// Invertovaná mapa: Windows Virtual Key code → macOS key code.
/// Viewer posílá Windows VK kódy, agent je převádí na macOS keycodes pro CGEvent.
struct KeyCodeMapper {

    /// Windows VK code → macOS key code
    static let vkToMac: [Int: UInt16] = [
        0x41: 0x00, // A
        0x53: 0x01, // S
        0x44: 0x02, // D
        0x46: 0x03, // F
        0x48: 0x04, // H
        0x47: 0x05, // G
        0x5A: 0x06, // Z
        0x58: 0x07, // X
        0x43: 0x08, // C
        0x56: 0x09, // V
        0x42: 0x0B, // B
        0x51: 0x0C, // Q
        0x57: 0x0D, // W
        0x45: 0x0E, // E
        0x52: 0x0F, // R
        0x59: 0x10, // Y
        0x54: 0x11, // T
        0x31: 0x12, // 1
        0x32: 0x13, // 2
        0x33: 0x14, // 3
        0x34: 0x15, // 4
        0x36: 0x16, // 6
        0x35: 0x17, // 5
        0xBB: 0x18, // = (VK_OEM_PLUS)
        0x39: 0x19, // 9
        0x37: 0x1A, // 7
        0xBD: 0x1B, // - (VK_OEM_MINUS)
        0x38: 0x1C, // 8
        0x30: 0x1D, // 0
        0xDD: 0x1E, // ] (VK_OEM_6)
        0x4F: 0x1F, // O
        0x55: 0x20, // U
        0xDB: 0x21, // [ (VK_OEM_4)
        0x49: 0x22, // I
        0x50: 0x23, // P
        0x0D: 0x24, // Return (VK_RETURN)
        0x4C: 0x25, // L
        0x4A: 0x26, // J
        0xDE: 0x27, // ' (VK_OEM_7)
        0x4B: 0x28, // K
        0xBA: 0x29, // ; (VK_OEM_1)
        0xDC: 0x2A, // \ (VK_OEM_5)
        0xBC: 0x2B, // , (VK_OEM_COMMA)
        0xBF: 0x2C, // / (VK_OEM_2)
        0x4E: 0x2D, // N
        0x4D: 0x2E, // M
        0xBE: 0x2F, // . (VK_OEM_PERIOD)
        0x09: 0x30, // Tab (VK_TAB)
        0x20: 0x31, // Space (VK_SPACE)
        0xC0: 0x32, // ` (VK_OEM_3)
        0x08: 0x33, // Backspace (VK_BACK)
        0x1B: 0x35, // Escape (VK_ESCAPE)
        // Modifikátory
        0x5B: 0x37, // Left Win → Command (VK_LWIN)
        0xA0: 0x38, // Left Shift (VK_LSHIFT)
        0x14: 0x39, // Caps Lock (VK_CAPITAL)
        0xA4: 0x3A, // Left Alt → Option (VK_LMENU)
        0xA2: 0x3B, // Left Ctrl (VK_LCONTROL)
        0x11: 0x3B, // VK_CONTROL → Left Ctrl (generic)
        0xA1: 0x3C, // Right Shift (VK_RSHIFT)
        0xA5: 0x3D, // Right Alt → Option (VK_RMENU)
        0xA3: 0x3E, // Right Ctrl (VK_RCONTROL)
        0x10: 0x38, // VK_SHIFT → Left Shift (generic)
        0x12: 0x3A, // VK_MENU → Left Option (generic Alt)
        // F klávesy
        0x70: 0x7A, // F1
        0x71: 0x78, // F2
        0x72: 0x63, // F3
        0x73: 0x76, // F4
        0x74: 0x60, // F5
        0x75: 0x61, // F6
        0x76: 0x62, // F7
        0x77: 0x64, // F8
        0x78: 0x65, // F9
        0x79: 0x6D, // F10
        0x7A: 0x67, // F11
        0x7B: 0x6F, // F12
        // Šipky a navigace
        0x25: 0x7B, // Left Arrow (VK_LEFT)
        0x27: 0x7C, // Right Arrow (VK_RIGHT)
        0x28: 0x7D, // Down Arrow (VK_DOWN)
        0x26: 0x7E, // Up Arrow (VK_UP)
        0x24: 0x73, // Home (VK_HOME)
        0x23: 0x77, // End (VK_END)
        0x21: 0x74, // Page Up (VK_PRIOR)
        0x22: 0x79, // Page Down (VK_NEXT)
        0x2E: 0x75, // Delete (VK_DELETE)
        0x2D: 0x72, // Insert (VK_INSERT)
    ]

    /// Převede Windows VK code na macOS key code
    static func macKeyCode(fromVK vkCode: Int) -> UInt16? {
        return vkToMac[vkCode]
    }
}
